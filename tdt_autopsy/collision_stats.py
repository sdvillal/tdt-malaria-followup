        #   - amb: ambiguous dataset (i.e. labelled but uncertain outcome)
        #   - unl: the 1056 mols competition dataset
        #   - scr: the 5.5+M mols commercial dataset
        'num_amb_mols',
        'amb_counts',
        'num_unl_mols',
